return {
  name = "agent",
  cmds = {
    function(self, args, input)
      if not args or type(args) ~= "table" then
        return {
          status = "error",
          data = "Invalid arguments provided",
        }
      end

      local mode = args.mode or "reason"
      local chat = self.chat

      if mode == "reason" then
        return process_reason_mode(args, chat)
      elseif mode == "execute" then
        return process_execute_mode(args, chat)
      elseif mode == "reflect" then
        return process_reflect_mode(args, chat)
      else
        return {
          status = "error",
          data = fmt("Unknown mode: %s", mode),
        }
      end
    end,
  },
  schema = {
    ["function"] = {
      name = "agent",
      description = "Tree of Thoughts AI agent that generates multiple solution approaches, evaluates them, and executes the best one through deliberate reasoning",
      parameters = {
        type = "object",
        properties = {
          mode = {
            type = "string",
            enum = { "reason", "execute", "reflect" },
            description = "Operation mode: 'reason' for tree of thoughts analysis, 'execute' for step execution, 'reflect' for plan refinement",
          },
          task = {
            type = "string",
            description = "The problem to solve (required for reason mode)",
          },
          num_thoughts = {
            type = "number",
            description = "Number of thought processes to generate (2-10, default: 4)",
          },
          confidence_threshold = {
            type = "number",
            description = "Minimum confidence required to proceed (0-1, default: 0.8)",
          },
          plan_id = {
            type = "string",
            description = "Plan identifier (required for execute and reflect modes)",
          },
        },
        required = { "mode" },
      },
    },
    type = "function",
  },
  handlers = {
    on_exit = function(agent)
      log:trace("[Agent] Cleanup completed")
    end,
  },
  output = {
    prompt = function(self, agent)
      local args = self.args
      local mode = args.mode or "reason"

      if mode == "reason" then
        return fmt("Generate tree of thoughts analysis for: %s?", args.task or "unknown task")
      elseif mode == "execute" then
        return fmt("Execute next step of plan %s?", args.plan_id or "unknown")
      elseif mode == "reflect" then
        return fmt("Perform tree of thoughts reflection on plan %s?", args.plan_id or "unknown")
      end

      return "Execute tree of thoughts agent?"
    end,

    success = function(self, agent, cmd, stdout)
      local data = stdout[1]

      if type(data) == "table" and data.phase == "reasoning_complete" then
        local plan = data.plan
        local analysis = data.analysis

        local output = fmt(
          [[<agentTool>🧠 **Tree of Thoughts Agent - Analysis Complete**

**Task:** %s
**Selected Approach:** %s (Confidence: %.1f%%)
**Thought Processes Considered:** %d

**📋 Execution Plan Created:**
- **Plan ID:** `%s`
- **Steps:** %d
- **Estimated Success Rate:** %.1f%%

**Tree of Thoughts Analysis:**
%s

**Next:** Use execute mode with plan_id: `%s`</agentTool>]],
          plan.task,
          plan.approach_name,
          plan.confidence * 100,
          #(analysis.thought_processes or {}),
          plan.id,
          #plan.steps,
          plan.confidence * 100,
          generate_analysis_summary(analysis),
          plan.id
        )

        local user_msg = output:gsub("<agentTool>", ""):gsub("</agentTool>", "")
        agent.chat:add_tool_output(self, output, user_msg)
      elseif type(data) == "table" and data.phase == "step_executed" then
        local step_result = data.step_result
        local progress = data.progress

        local status_icon = step_result.success and "✅" or "❌"
        local output = fmt(
          [[<agentTool>%s **Tree of Thoughts Agent - Step Executed**

**Step %d/%d:** %s
**Status:** %s
**Tools Used:** %s

**Progress:** %d completed, %d failed
**Next:** %s</agentTool>]],
          status_icon,
          progress.current_step,
          progress.total_steps,
          step_result.action,
          step_result.success and "Success" or "Failed",
          table.concat(step_result.tools_attempted or {}, ", "),
          progress.completed,
          progress.failed,
          progress.next_step and fmt("Step %d: %s", progress.current_step + 1, progress.next_step.action)
            or "Execution complete"
        )

        local user_msg = output:gsub("<agentTool>", ""):gsub("</agentTool>", "")
        agent.chat:add_tool_output(self, output, user_msg)
      else
        local output = fmt("<agentTool>🤖 Tree of Thoughts Agent: %s</agentTool>", vim.inspect(data))
        local user_msg = output:gsub("<agentTool>", ""):gsub("</agentTool>", "")
        agent.chat:add_tool_output(self, output, user_msg)
      end
    end,

    error = function(self, agent, cmd, stderr)
      local errors = table.concat(stderr, "\n")
      local output = fmt("<agentTool>❌ **Tree of Thoughts Agent Error:** %s</agentTool>", errors)
      agent.chat:add_tool_output(self, output)
    end,

    rejected = function(self, agent, cmd)
      agent.chat:add_tool_output(self, "**Tree of Thoughts Agent:** Operation cancelled by user")
    end,
  },
}

