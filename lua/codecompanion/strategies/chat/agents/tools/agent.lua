---REASON MODE: Generate tree of thoughts analysis and create execution plan
---@param args table Arguments (task, num_thoughts, confidence_threshold)
---@param chat table Chat context
---@return table Result with plan or error
local function process_reason_mode(args, chat)
  local task = args.task
  local num_thoughts = args.num_thoughts or 4
  local confidence_threshold = args.confidence_threshold or 0.8

  -- Validate inputs
  if not task or type(task) ~= "string" or task:len() == 0 then
    return {
      status = "error",
      data = "Task must be a non-empty string",
    }
  end

  if num_thoughts < 2 or num_thoughts > 10 then
    return {
      status = "error",
      data = "num_thoughts must be between 2 and 10",
    }
  end

  local state = get_agent_state(chat)
  local available_tools = get_available_tools(chat)

  log:info("[Agent] Starting tree of thoughts reasoning for: %s", task)

  -- Generate tree of thoughts prompt
  local prompt = create_tree_of_thoughts_prompt(task, available_tools, num_thoughts)

  -- For now, simulate LLM response with a structured example
  -- In real implementation, this would call the LLM with the prompt
  local simulated_response = generate_example_tree_of_thoughts(task, available_tools, num_thoughts)

  -- Parse the response
  local analysis = parse_tree_of_thoughts_response(vim.json.encode(simulated_response))
  if not analysis then
    return {
      status = "error",
      data = "Failed to generate valid tree of thoughts analysis",
    }
  end

  -- Select best approach
  local best_approach = select_best_approach(analysis)
  if not best_approach or best_approach.confidence < confidence_threshold then
    return {
      status = "error",
      data = {
        error_type = "insufficient_confidence",
        message = "No approach meets confidence threshold",
        best_confidence = best_approach and best_approach.confidence or 0,
        threshold = confidence_threshold,
        approaches_considered = #analysis.thought_processes,
      },
    }
  end

  -- Create execution plan
  local plan_id = fmt("tot_plan_%d_%d", os.time(), math.random(1000, 9999))
  local plan = {
    id = plan_id,
    task = task,
    created_at = os.date("%Y-%m-%d %H:%M:%S"),
    status = "created",
    approach_name = best_approach.name,
    confidence = best_approach.confidence,
    steps = best_approach.steps,
    all_thought_processes = analysis.thought_processes,
    selected_thought_id = best_approach.id,
    execution_state = {
      step_index = 1,
      completed_steps = 0,
      failed_steps = 0,
      step_results = {},
    },
    reflection_history = {},
  }

  -- Store plan
  state.active_plans[plan_id] = plan

  -- Record reasoning session
  table.insert(state.reasoning_sessions, {
    task = task,
    timestamp = os.time(),
    approaches_generated = #analysis.thought_processes,
    selected_approach = best_approach.id,
    confidence = best_approach.confidence,
    plan_id = plan_id,
  })

  log:info(
    "[Agent] Created plan %s with approach '%s' (confidence: %.2f)",
    plan_id,
    best_approach.name,
    best_approach.confidence
  )

  return {
    status = "success",
    data = {
      phase = "reasoning_complete",
      plan = plan,
      analysis = analysis,
      tree_of_thoughts_prompt = prompt,
    },
  }
end

---EXECUTE MODE: Execute the next step of a plan
---@param args table Arguments (plan_id)
---@param chat table Chat context
---@return table Execution result
local function process_execute_mode(args, chat)
  local plan_id = args.plan_id

  if not plan_id then
    return {
      status = "error",
      data = "plan_id is required",
    }
  end

  local state = get_agent_state(chat)
  local plan = state.active_plans[plan_id]

  if not plan then
    return {
      status = "error",
      data = fmt("Plan '%s' not found", plan_id),
    }
  end

  local execution_state = plan.execution_state
  local steps = plan.steps or {}

  -- Check if execution is complete
  if execution_state.step_index > #steps then
    plan.status = "completed"
    plan.completed_at = os.date("%Y-%m-%d %H:%M:%S")

    return {
      status = "success",
      data = {
        phase = "execution_complete",
        plan = plan,
        summary = {
          total_steps = #steps,
          completed = execution_state.completed_steps,
          failed = execution_state.failed_steps,
          success_rate = execution_state.completed_steps / #steps,
        },
      },
    }
  end

  -- Execute current step
  local current_step = steps[execution_state.step_index]
  local step_result = execute_plan_step(current_step, plan, chat)

  -- Update execution state
  execution_state.step_results[execution_state.step_index] = step_result
  if step_result.success then
    execution_state.completed_steps = execution_state.completed_steps + 1
  else
    execution_state.failed_steps = execution_state.failed_steps + 1
  end
  execution_state.step_index = execution_state.step_index + 1

  -- Update plan status
  plan.status = "executing"

  -- Log execution
  table.insert(state.execution_logs, {
    plan_id = plan_id,
    step_number = current_step.step_number,
    success = step_result.success,
    timestamp = os.time(),
  })

  log:info(
    "[Agent] Executed step %d/%d for plan %s (Success: %s)",
    current_step.step_number,
    #steps,
    plan_id,
    step_result.success
  )

  return {
    status = "success",
    data = {
      phase = "step_executed",
      plan = plan,
      step_result = step_result,
      progress = {
        current_step = execution_state.step_index - 1,
        total_steps = #steps,
        completed = execution_state.completed_steps,
        failed = execution_state.failed_steps,
        next_step = execution_state.step_index <= #steps and steps[execution_state.step_index] or nil,
      },
    },
  }
end

---REFLECT MODE: Analyze execution and refine plan using tree of thoughts
---@param args table Arguments (plan_id)
---@param chat table Chat context
---@return table Reflection result
local function process_reflect_mode(args, chat)
  local plan_id = args.plan_id

  if not plan_id then
    return {
      status = "error",
      data = "plan_id is required",
    }
  end

  local state = get_agent_state(chat)
  local plan = state.active_plans[plan_id]

  if not plan then
    return {
      status = "error",
      data = fmt("Plan '%s' not found", plan_id),
    }
  end

  -- Perform tree of thoughts reflection
  local reflection_result = perform_plan_reflection(plan, state)

  -- Apply refinements if confidence is high
  local refinements_applied = 0
  if reflection_result.reflection_confidence > 0.7 then
    refinements_applied = apply_plan_refinements(plan, reflection_result.refinements_suggested)
  end

  -- Update plan
  plan.status = "reflected"
  plan.reflected_at = os.date("%Y-%m-%d %H:%M:%S")
  table.insert(plan.reflection_history, reflection_result)

  -- Record reflection
  table.insert(state.reflection_results, {
    plan_id = plan_id,
    timestamp = os.time(),
    approaches_considered = #reflection_result.reflection_approaches,
    refinements_applied = refinements_applied,
  })

  log:info("[Agent] Reflected on plan %s (Refinements applied: %d)", plan_id, refinements_applied)

  return {
    status = "success",
    data = {
      phase = "reflection_complete",
      plan = plan,
      reflection = reflection_result,
      refinements_applied = refinements_applied,
    },
  }
end

