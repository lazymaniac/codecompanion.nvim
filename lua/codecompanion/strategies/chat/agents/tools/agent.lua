---Prepare arguments for a specific tool based on step context
---@param tool_name string Name of the tool
---@param step table Current step being executed
---@param plan AgentPlan The overall plan
---@return table|nil Tool arguments
local function prepare_tool_arguments(tool_name, step, plan)
  local task = plan.task or ""
  local action = step.action or ""
  local description = step.description or ""

  if tool_name == "web_search" then
    return {
      query = extract_key_terms(task .. " " .. description),
      include_domains = {},
    }
  elseif tool_name == "grep_search" then
    return {
      query = extract_key_terms(action .. " " .. description),
      include_pattern = "**/*",
    }
  elseif tool_name == "file_search" then
    return {
      query = "**/*",
    }
  elseif tool_name == "read_file" then
    return {
      filepath = "README.md", -- Could be smarter about file selection
      start_line_number_base_zero = 0,
      end_line_number_base_zero = -1,
    }
  elseif tool_name == "create_file" then
    return {
      filepath = generate_filename(step, plan),
      content = generate_file_content(step, plan),
    }
  elseif tool_name == "insert_edit_into_file" then
    return {
      filepath = "generated_file.lua",
      explanation = fmt("Step %d: %s", step.step_number, step.description),
      code = generate_code_content(step, plan),
    }
  elseif tool_name == "cmd_runner" then
    return {
      cmd = generate_command(step, plan),
      flag = step.action:match("test") and "testing" or nil,
    }
  end

  return nil
end

---Execute a single step from the plan
---@param step table The step to execute
---@param plan AgentPlan The plan being executed
---@param chat table Chat context for tool access
---@return table Execution result
local function execute_plan_step(step, plan, chat)
  local result = {
    success = false,
    step_number = step.step_number,
    action = step.action,
    tools_attempted = {},
    outputs = {},
    errors = {},
    execution_time = os.time(),
  }

  -- Execute each tool specified in the step
  for _, tool_name in ipairs(step.tools or {}) do
    local tool_result = {
      tool = tool_name,
      success = false,
      output = nil,
      error = nil,
    }

    -- Check if tool is available
    if not chat.tools or not chat.tools.in_use[tool_name] then
      tool_result.error = fmt("Tool '%s' not available", tool_name)
    else
      -- Prepare tool arguments based on step context
      local args = prepare_tool_arguments(tool_name, step, plan)
      if args then
        tool_result.success = true
        tool_result.args = args
        tool_result.output = fmt("Prepared %s with args for: %s", tool_name, step.description)
      else
        tool_result.error = fmt("Could not prepare arguments for %s", tool_name)
      end
    end

    table.insert(result.tools_attempted, tool_result)

    if tool_result.success then
      table.insert(result.outputs, tool_result.output)
    else
      table.insert(result.errors, tool_result.error)
    end
  end

  -- Determine overall step success
  result.success = #result.errors == 0

  return result
end

