---Parse the LLM's tree of thoughts response
---@param response_json string JSON response from LLM
---@return table|nil Parsed thought processes and meta-analysis
local function parse_tree_of_thoughts_response(response_json)
  local success, parsed = pcall(vim.json.decode, response_json)

  if not success then
    log:error("[Agent] Failed to parse tree of thoughts JSON: %s", response_json)
    return nil
  end

  if not parsed.thought_processes or #parsed.thought_processes == 0 then
    log:error("[Agent] No thought processes found in response")
    return nil
  end

  -- Validate and enhance each thought process
  for i, thought in ipairs(parsed.thought_processes) do
    -- Calculate overall confidence if not provided
    if not thought.confidence and thought.evaluation then
      local eval = thought.evaluation
      thought.confidence = (
        (eval.feasibility or 0) * 0.3
        + (eval.completeness or 0) * 0.3
        + (eval.tool_fit or 0) * 0.2
        + (eval.innovation or 0) * 0.1
        + (1 - (eval.risk_level or 0)) * 0.1
      )
    end

    -- Ensure required fields exist
    thought.id = thought.id or fmt("approach_%d", i)
    thought.name = thought.name or fmt("Approach %d", i)
    thought.risks = thought.risks or {}
    thought.mitigations = thought.mitigations or {}
  end

  return parsed
end

---Select the best approach from tree of thoughts analysis
---@param analysis table Parsed tree of thoughts response
---@return TreeOfThoughtsPath|nil Selected approach
local function select_best_approach(analysis)
  if not analysis or not analysis.thought_processes then
    return nil
  end

  -- If meta-analysis provides recommendation, use it
  if analysis.meta_analysis and analysis.meta_analysis.recommendation then
    local rec_id = analysis.meta_analysis.recommendation.best_approach_id
    for _, thought in ipairs(analysis.thought_processes) do
      if thought.id == rec_id then
        return thought
      end
    end
  end

  -- Fallback: select highest confidence approach
  local best = nil
  local best_score = 0

  for _, thought in ipairs(analysis.thought_processes) do
    local score = thought.confidence or 0
    if score > best_score then
      best_score = score
      best = thought
    end
  end

  return best
end

